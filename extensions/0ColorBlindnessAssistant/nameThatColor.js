            ndf2 = Math.abs(Math.pow(h - this.names[i][6], 2)) +
                Math.pow(s - this.names[i][7], 2) +
                Math.abs(Math.pow(l - this.names[i][8], 2));
            ndf = ndf1 + ndf2 * 2;
