    // Adopted from: Farbtastic 1.2.
    // http://acko.net/dev/farbtastic
    hsl: function(color) {
        let rgb = [
            parseInt("0x" + color.substring(1, 3), 16) / 255,
            parseInt("0x" + color.substring(3, 5), 16) / 255,
            parseInt("0x" + color.substring(5, 7), 16) / 255
        ];
        let min,
            max,
            delta,
            h,
            s,
            l;
        let r = rgb[0],
            g = rgb[1],
            b = rgb[2];

        min = Math.min(r, Math.min(g, b));
        max = Math.max(r, Math.max(g, b));
        delta = max - min;
        l = (min + max) / 2;

        s = 0;
        if (l > 0 && l < 1) {
            s = delta / (l < 0.5 ? (2 * l) : (2 - 2 * l));
        }

        h = 0;
        if (delta > 0) {
            if (max === r && max != g) {
                h += (g - b) / delta;
            }
            if (max === g && max != b) {
                h += (2 + (b - r) / delta);
            }
            if (max === b && max != r) {
                h += (4 + (r - g) / delta);
            }
            h /= 6;
        }
        return [
            parseInt(h * 255, 10),
            parseInt(s * 255, 10),
            parseInt(l * 255, 10)
        ];
    },

    // Adopted from: Farbtastic 1.2.
